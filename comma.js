/*In JavaScript, the comma operator (,) allows 
 to evaluate multiple expressions within a single statement. 
 It evaluates each of its operands (expressions) from 
 left to right and returns the value of the last operand. 
 The comma operator is often used in situations where multiple expressions 
 need to be evaluated sequentially, such as in variable declarations, loop headers, 
 function arguments, and within parentheses.example:*/
 let a = 5, b = 10, c = 15; 
console.log(a); 
console.log(b); 
console.log(c);

